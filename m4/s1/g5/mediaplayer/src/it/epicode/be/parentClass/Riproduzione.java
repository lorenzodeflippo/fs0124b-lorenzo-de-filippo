package it.epicode.be.parentClass;

// Definisco l'interfaccia Riproduzione che fornisce metodi per controllare la riproduzione dei multimedia
public interface Riproduzione {

   // Metodo per aumentare il volume della riproduzione
   void plusVol();

   // Metodo per diminuire il volume della riproduzione
   void minVol();

   // Metodo per avviare la riproduzione multimediale
   void play();
}
